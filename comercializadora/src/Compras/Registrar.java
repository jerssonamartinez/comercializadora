    public void insertar() {

        if (conn != null) {
            Statement s = null;
            ResultSet I = consulta();
            
            try {   
                adv.setText(advertencia());
     
                //Si I no es nulo, es porque se arrojo un query por lo tanto. existe un dato con esa id
                //T query que consulta si existe un producto con ese cod
                String T[] = new String[1];
                
                while (I.next()) {
                    T[0] = I.getString(1);
                }
                if (T[0] == null)  {
                    s = conn.createStatement();
                    s.executeQuery("INSERT INTO compra "
                            + "(codprod, nombre, precioUnit, cantidad, total) VALUES ("
                            + codProd.getText()
                            + ",'" + nombre.getText()
                            + "'," + precio.getText()
                            + "," + cantidad.getText()
                            + "," + total.getText()
                            + ");INSERT INTO inventario "
                            + "(codprod, producto,cantidad,preciounit,saldo) VALUES ("
                            + codProd.getText()
                            + ",'" + nombre.getText()
                            + "'," + cantidad.getText()
                            + "," + precio.getText()
                            + "," + total.getText() +")");
                }else{
                s = conn.createStatement();
                s.executeQuery("UPDATE inventario SET  cantidad=cantidad+"+cantidad.getText()+
                            " WHERE codProd="+codProd.getText()+";UPDATE inventario SET "
                            + "precioUnit=((saldo+"+total.getText()+")/cantidad) WHERE "
                            + "codProd="+codProd.getText() + ";UPDATE inventario SET  "
                            + "saldo=precioUnit*cantidad" +
                            " WHERE codProd="+codProd.getText());
                }
            } catch (Exception ex) {
                System.out.println("insertado");
            }
            // desconectar();
        }
    }
    public ResultSet consulta() {
        ResultSet n = null;
        try {
            Statement st = conn.createStatement();
            n = st.executeQuery("SELECT nombre FROM Compra WHERE codprod = " + codProd.getText());

        } catch (SQLException ex) {
            ex.getMessage();
        }
        return n;
    }
    
    public String advertencia() throws SQLException {
        String S = null;
             
        if (nombre.getText().isEmpty()
                || precio.getText().isEmpty() || codProd.getText().isEmpty()
                || cantidad.getText().isEmpty() || total.getText().isEmpty()) {
            S = "<html>Error al guardar el dato. Existen campos vac√≠os</html>";
        }else{
        S = " ";
        }
        return S;
    }
