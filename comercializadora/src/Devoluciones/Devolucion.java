    public void insertar() {

        if (conn != null) {
            Statement s = null;
            ResultSet I = consulta();
            try {
                adv.setText(advertencia());

                //Si I no es nulo, es porque se arrojo un query por lo tanto. existe un dato con esa id
                String T[] = new String[1];
                while (I.next()) {
                    T[0] = I.getString(1);
                }

                if (T[0] != null) {
                    s = conn.createStatement();
                    switch (caso) {
                        case 1:
                            s.executeQuery("UPDATE inventario SET  cantidad=cantidad-"
                                    + cantidad.getText()
                                    + " WHERE codProd=" + codProd.getText() + ";UPDATE inventario SET  "
                                    + "saldo=saldo-(cantidad*preciounit)"
                                    + " WHERE codProd=" + codProd.getText()
                                    + ";INSERT INTO compra "
                                    + "(codprod, nombre, precioUnit, cantidad, total) VALUES ("
                                    + codProd.getText()
                                    + ",' devolucion"
                                    + "',-" + T[0]
                                    + ",-" + cantidad.getText()
                                    + ",-" + cantidad.getText() + "*" + T[0]
                                    + ")");
                            break;
                        case 2:
                            s.executeQuery("UPDATE inventario SET  cantidad=cantidad+"
                                    + cantidad.getText()
                                    + " WHERE codProd=" + codProd.getText() + ";UPDATE inventario SET  "
                                    + "saldo=saldo+(cantidad*preciounit)"
                                    + " WHERE codProd=" + codProd.getText()
                                    + ";INSERT INTO venta "
                                    + "(codprod, nombre, precioUnit, cantidad, total) VALUES ("
                                    + codProd.getText()
                                    + ",' devolucion"
                                    + "',-" + T[0]
                                    + ",-" + cantidad.getText()
                                    + ",-" + cantidad.getText() + "*" + T[0]
                                    + ")");
                            break;
                        case 0:
                            break;

                    }
                }
            } catch (Exception ex) {
                System.out.println("insertado");
            }
        }
    }

    public ResultSet consulta() {
        ResultSet n = null;
        try {
            Statement st = conn.createStatement();
            n = st.executeQuery("SELECT preciounit FROM inventario WHERE codProd = " + codProd.getText());

        } catch (SQLException ex) {
            ex.getMessage();
        }
        return n;
    }

    public String advertencia() throws SQLException {
        String S = null;

        ResultSet Q = consulta();
        //T query que consulta si existe un dato con ese cod
        String T[] = new String[1];

        while (Q.next()) {
            T[0] = Q.getString(1);
        }

        if (codProd.getText().isEmpty() || cantidad.getText().isEmpty()) {
            S = "<html>Existen campos vac√≠os</html>";
        } else if (T[0] == null) {
            S = "<html>Error al devolver el producto, NO existe un producto con esa ID</html>";
        } else {
            S = " ";
        }
        return S;
    }

